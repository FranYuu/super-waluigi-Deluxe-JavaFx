package enumeration;

/**
 * Enumération qui référence les quatre Direction possible de déplacement (haut, bas, gauche, droite).
 * @author Calvet & Gonçalves
 */
public enum Direction {
    /**
     * Direction pour le haut.
     */
    UP,
    
    /**
     * Direction pour le bas.
     */
    DOWN,
    
    /**
     * Direction pour la gauche.
     */
    LEFT,
    
    /**
     * Direction pour la droite.
     */
    RIGHT,
    
    /**
     * Si il y a n'y a pas de contact
     */
    NEUTRAL,
}
